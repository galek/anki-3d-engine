#ifndef STD_TYPES_H
#define STD_TYPES_H


#ifndef uchar
	typedef unsigned char uchar;
#endif

#ifndef uint
	typedef unsigned int uint;
#endif

#ifndef ushort
	typedef unsigned short int ushort;
#endif

#ifndef ulong
	typedef unsigned long int ulong;
#endif


#if !defined(NULL)
	#define NULL 0
#endif


#endif
