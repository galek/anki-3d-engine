#include "Event.h"
#include "Assert.h"


namespace Event {


//======================================================================================================================
// Constructor                                                                                                         =
//======================================================================================================================
Event::Event(EventType type_, uint startTime_, int duration_):
	type(type_),
	startTime(startTime_),
	duration(duration_)
{}


//======================================================================================================================
// operator=                                                                                                           =
//======================================================================================================================
Event& Event::operator=(const Event& b)
{
	type = b.type;
	startTime = b.startTime;
	duration = b.duration;
	return *this;
}


//======================================================================================================================
// update                                                                                                              =
//======================================================================================================================
void Event::update(uint timeUpdate)
{
	ASSERT(!isFinished());

	updateSp(timeUpdate);

	duration -= timeUpdate;
}


} // end namespace
