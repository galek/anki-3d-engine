#include "light.h"
#include "collision.h"
#include "renderer.h"


//=====================================================================================================================================
// RenderSphere                                                                                                                       =
//=====================================================================================================================================
static void RenderSphere( const mat4_t& tsl, const vec3_t& col )
{
	glPushMatrix();
	r::MultMatrix( tsl );

	r::Color3( col );
	r::dbg::RenderSphere( 1.0/8.0, 8 );

	glPopMatrix();
}


//=====================================================================================================================================
// Render                                                                                                                             =
//=====================================================================================================================================
void point_light_t::Render()
{
	RenderSphere( transformation_wspace, diffuse_color );
}


//=====================================================================================================================================
// Render                                                                                                                             =
//=====================================================================================================================================
void spot_light_t::Render()
{
	RenderSphere( transformation_wspace, diffuse_color );
}
